public class DortIslem {
    public int topla(int sayi1,int sayi2){
        return sayi1+sayi2;
    }

    public int cikma(int sayi1,int sayi2){
        return sayi1-sayi2;
    }

    public int vur(int sayi1,int sayi2){
        return sayi1*sayi2;
    }

    public int bol(int sayi1,int sayi2){
        return sayi1/sayi2;
    }
}
